/* Aggregates                                                    */
/* ═══════════════════════════════════════════════════════════ */

export function totalBucketBalance(buckets: Bucket[]): number {
  return buckets.reduce((sum, b) => sum + (b.currentAmount || 0), 0);
}

export function totalBucketTarget(buckets: Bucket[]): number {
  return buckets.reduce((sum, b) => sum + (b.targetAmount || 0), 0);
}

export function totalMonthlyContributions(buckets: Bucket[]): number {
  return buckets.reduce((sum, b) => sum + (b.monthlyContribution || 0), 0);
}
