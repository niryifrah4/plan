/** A single monthly contribution record — populated during check-in */
export interface BucketContribution {
  /** YYYY-MM format, e.g. "2026-04" */
  month: string;
  /** Planned contribution for that month (from bucket.monthlyContribution at the time) */
  planned: number;
  /** What the client actually confirmed they deposited */
  actual: number;
  /** ISO timestamp of when the check-in was confirmed */
  confirmedAt: string;
  /** Optional note from the client */
  note?: string;
}

/** Status of a goal — drives visual styling + recommendations */
export type BucketStatus = "ahead" | "on_track" | "behind" | "at_risk";

/** Priority level — affects sort order & recommendation urgency */
export type BucketPriority = "high" | "medium" | "low";

/** Snapshot of a bucket balance at a point in time */
export interface BucketSnapshot {
  date: string;
  balance: number;
  source: "manual" | "masleka" | "check-in";
}

/** The core Bucket entity */
export interface Bucket {
  /** Stable ID */
  id: string;
  /** Display name, e.g. "דירה לילדים" */
  name: string;
  /** Google Material Symbols icon name */
  icon: string;
  /** Hex color for visual identification — the "color" of the money */
  color: string;

  /* ── Target ───────────────────────── */
  targetAmount: number;
  /** ISO date (YYYY-MM-DD) of target completion */
  targetDate: string;
  priority: BucketPriority;

  /* ── Funding ──────────────────────── */
  currentAmount: number;
  monthlyContribution: number;
  /** Optional: instrument key from the instruments catalog (for tax/return context) */
  fundingSource?: string;

  /* ── Return tracking ──────────────── */
  expectedAnnualReturn: number;
  actualAnnualReturn?: number;

  /* ── Check-in history ─────────────── */
  contributionHistory: BucketContribution[];
  balanceSnapshots: BucketSnapshot[];

  /* ── Initial cash ────────────────── */
  /** One-time lump sum already in hand at goal creation (e.g. ₪70k from selling old car).
   *  Counts toward progress on top of linkedAssets and ongoing contributions. */
  initialCash?: number;

  /* ── Meta ─────────────────────────── */
  createdAt: string;
  updatedAt: string;
  notes?: string;
  archived?: boolean;
  /** Business / personal / mixed tag. Undefined = personal. */
  scope?: "personal" | "business" | "mixed";

  /**
   * Goal category — financial (savings target, retirement, real estate)
   * vs family (life events: bar mitzvah, kid age 18, wedding).
   * Default treated as "financial" when undefined for backwards compat.
   */
  category?: "financial" | "family";

  /**
   * Auto-generated metadata. When the system creates a goal automatically
   * (e.g. bar mitzvah from a kid's DOB), these tags let the UI show the
   * "auto" pill and prevent duplicate generation on each load.
   */
  /**
   * Recurrence pattern. Per Nir 2026-04-27: a car/vacation/celebration is
   * NOT a one-shot — it's a cycle. When set, the goal "renews" on
   * completion: targetDate += periodYears, currentAmount cycles back to
   * 0, cycleNumber increments. Display shows "מחזור N" badge.
   */
  recurrence?: {
    /** Years between cycles (e.g. car every 7, vacation every 1). */
    periodYears: number;
    /** Optional: bump targetAmount each cycle to keep up with inflation. */
    inflationPctPerCycle?: number;
    /** 1-based current cycle number. Increments on auto-rollover. */
    cycleNumber?: number;
  };

  autoGenerated?: {
    /** What seeded this. "system" = default essentials (e.g. emergency fund). */
    source: "kids_savings" | "retirement" | "mortgage" | "manual" | "system";
    /** Reference id in the source store (e.g. kid id). */
    sourceRefId?: string;
    /** Sub-type — useful for kids: "bar_mitzvah" | "age_18" | "age_21" | "emergency_fund" */
    subKind?: string;
    /** True if user has edited this goal — stop auto-overwriting from now on. */
    userEdited?: boolean;
  };

  /**
   * Emergency-fund-specific fields (added 2026-04-28). When `isEmergency`
   * is true, the /goals UI shows a 3/6 month coverage selector instead of
   * the regular target editor; targetAmount is recomputed = monthlyIncome
   * × coverageMonths whenever income changes.
   */
  isEmergency?: boolean;
  coverageMonths?: 3 | 4 | 5 | 6;
}

