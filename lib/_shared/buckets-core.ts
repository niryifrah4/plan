/** Pick a color deterministically from the palette */
export function pickColor(seed: string | number): string {
  const s = String(seed);
  let hash = 0;
  for (let i = 0; i < s.length; i++) {
    hash = (hash << 5) - hash + s.charCodeAt(i);
    hash |= 0;
  }
  return BUCKET_COLORS[Math.abs(hash) % BUCKET_COLORS.length];
}

/** Generate a stable ID for a new bucket */
export function generateBucketId(): string {
  return `bkt_${Date.now().toString(36)}_${Math.random().toString(36).slice(2, 8)}`;
}

