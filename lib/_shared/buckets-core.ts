export interface LegacyVisionGoal {
  id: string;
  name: string;
  icon?: string;
  targetAmount: number;
  targetDate: string;
  lumpToday: number;
  monthlyContrib: number;
  annualRate: number;
  priority: BucketPriority;
  instrument?: string;
}

export function migrateLegacyVisionGoals(legacy: LegacyVisionGoal[]): Bucket[] {
  const now = new Date().toISOString();
  return legacy.map((g, i) => ({
    id: g.id || generateBucketId(),
    name: g.name,
    icon: g.icon || "flag",
    color: pickColor(g.id || g.name || i),
    targetAmount: g.targetAmount || 0,
    targetDate: g.targetDate,
    priority: g.priority || "medium",
    currentAmount: g.lumpToday || 0,
    monthlyContribution: g.monthlyContrib || 0,
    fundingSource: g.instrument,
    expectedAnnualReturn: g.annualRate || 0.05,
    actualAnnualReturn: undefined,
    contributionHistory: [],
    balanceSnapshots: g.lumpToday > 0
      ? [{ date: now, balance: g.lumpToday, source: "manual" }]
      : [],
    createdAt: now,
    updatedAt: now,
  }));
}

export interface OnboardingGoalRow {
  name: string;
  cost: string;
  horizon: string;
  priority: string;
}

export function migrateOnboardingGoals(rows: OnboardingGoalRow[]): Bucket[] {
  const now = new Date().toISOString();
  return rows
    .filter(r => r.name && r.cost)
    .map((r, i) => {
      const cost = parseFloat(r.cost.replace(/[^\d.-]/g, "")) || 0;
      const horizonYears = parseFloat(r.horizon) || 5;
      const targetDate = new Date(
        Date.now() + horizonYears * 365.25 * 24 * 3600 * 1000
      ).toISOString().split("T")[0];
      return {
        id: generateBucketId(),
        name: r.name,
        icon: "flag",
        color: pickColor(i),
        targetAmount: cost,
        targetDate,
        priority: (r.priority === "גבוהה" ? "high" :
                   r.priority === "נמוכה" ? "low" : "medium") as BucketPriority,
        currentAmount: 0,
        monthlyContribution: 0,
        expectedAnnualReturn: 0.05,
        contributionHistory: [],
        balanceSnapshots: [],
        createdAt: now,
        updatedAt: now,
      };
    });
}

