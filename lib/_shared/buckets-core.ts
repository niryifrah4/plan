/** Create a new bucket with sensible defaults */
export function createBucket(
  input: Partial<Bucket> & Pick<Bucket, "name" | "targetAmount" | "targetDate">
): Bucket {
  const now = new Date().toISOString();
  const id = input.id || generateBucketId();
  return {
    id,
    name: input.name,
    icon: input.icon || "flag",
    color: input.color || pickColor(id),
    targetAmount: input.targetAmount,
    targetDate: input.targetDate,
    priority: input.priority || "medium",
    currentAmount: input.currentAmount || 0,
    monthlyContribution: input.monthlyContribution || 0,
    fundingSource: input.fundingSource,
    expectedAnnualReturn: input.expectedAnnualReturn ?? 0.05,
    actualAnnualReturn: input.actualAnnualReturn,
    contributionHistory: input.contributionHistory || [],
    balanceSnapshots: input.balanceSnapshots || (
      input.currentAmount && input.currentAmount > 0
        ? [{ date: now, balance: input.currentAmount, source: "manual" }]
        : []
    ),
    createdAt: input.createdAt || now,
    updatedAt: now,
    notes: input.notes,
    initialCash: input.initialCash || 0,
    isEmergency: input.isEmergency,
    coverageMonths: input.coverageMonths,
  };
}

/** Update a bucket in an array (immutable) */
export function updateBucket(buckets: Bucket[], id: string, patch: Partial<Bucket>): Bucket[] {
  return buckets.map(b =>
    b.id === id
      ? { ...b, ...patch, updatedAt: new Date().toISOString() }
      : b
  );
}

/** Remove a bucket (hard delete) */
export function removeBucket(buckets: Bucket[], id: string): Bucket[] {
  return buckets.filter(b => b.id !== id);
}

/** Record a monthly contribution check-in (immutable) */
export function recordCheckIn(
  bucket: Bucket,
  month: string,
  actual: number,
  note?: string
): Bucket {
  const now = new Date().toISOString();

  const history = bucket.contributionHistory.filter(c => c.month !== month);
  history.push({
    month,
    planned: bucket.monthlyContribution,
    actual,
    confirmedAt: now,
    note,
  });
  history.sort((a, b) => a.month.localeCompare(b.month));

  const newCurrent = bucket.currentAmount + actual;

  const snapshots: BucketSnapshot[] = [
    ...bucket.balanceSnapshots,
    { date: now, balance: newCurrent, source: "check-in" },
  ];

  return {
    ...bucket,
    currentAmount: newCurrent,
    contributionHistory: history,
    balanceSnapshots: snapshots,
    updatedAt: now,
  };
}

/** Record a balance snapshot (immutable) */
export function recordSnapshot(
  bucket: Bucket,
  balance: number,
  source: "manual" | "masleka" = "manual"
): Bucket {
  const now = new Date().toISOString();
  return {
    ...bucket,
    currentAmount: balance,
    balanceSnapshots: [
      ...bucket.balanceSnapshots,
      { date: now, balance, source },
    ],
    updatedAt: now,
  };
}

